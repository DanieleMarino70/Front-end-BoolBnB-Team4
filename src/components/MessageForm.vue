<script>
import axios from "axios";

export default {
  name: "MessageForm",

  props: {
    apartment_id: Number,
  },

  data() {
    return {
      message: {
        email: "",
        name: "",
        surname: "",
        text: "",
      },
    };
  },

  methods: {
    sendMessage() {
      const message = {
        apartment_id: this.apartment_id,
        email: this.message.email,
        name: this.message.name,
        surname: this.message.surname,
        text: this.message.text,
      };

      axios
        .post(
          `http://127.0.0.1:8000/api/apartments/${this.apartment_id}/messages`,
          message
        )
        .then((response) => {
          this.message.email = "";
          this.message.name = "";
          this.message.surname = "";
          this.message.text = "";
        })
        .catch((error) => {
          console.error(error);
        });
    },
  },
};
</script>

